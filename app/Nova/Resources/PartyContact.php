    {
        return [
            ID::make()->sortable(),

            BelongsTo::make('Party'),

            Select::make('Type')->options([
                'phone' => 'Phone Number',
                'email' => 'Email Address',
            ])->displayUsingLabels()->rules('required'),

            Text::make('Value')
                ->sortable()
                ->rules('required', 'max:255'),

            Boolean::make('Primary')
                ->sortable(),
        ];
    }

    /**
